# --- Result Reporting ---
# Usage: report_fix ID DESCRIPTION STATUS [DETAIL]
# STATUS: FIXED | SKIPPED | FAILED | DRY-RUN | INSTRUCTED
report_fix() {
    local id="$1"
    local description="$2"
    local status="$3"
    local detail="${4:-}"

    TOTAL=$((TOTAL + 1))
    case "$status" in
        FIXED)      FIXED_COUNT=$((FIXED_COUNT + 1)) ;;
        SKIPPED)    SKIPPED_COUNT=$((SKIPPED_COUNT + 1)) ;;
        FAILED)     FAILED_COUNT=$((FAILED_COUNT + 1)) ;;
        DRY-RUN)    SKIPPED_COUNT=$((SKIPPED_COUNT + 1)) ;;
        INSTRUCTED) FIXED_COUNT=$((FIXED_COUNT + 1)) ;;
    esac

    local classification="${FIX_REGISTRY[$id]:-UNKNOWN}"

    # JSON accumulation
    if $JSON_OUTPUT; then
        local json_entry
        json_entry=$(printf '{"id":"%s","classification":"%s","status":"%s","description":"%s"' \
            "$(json_escape "$id")" "$(json_escape "$classification")" \
            "$(json_escape "$status")" "$(json_escape "$description")")
        if [[ -n "$detail" ]]; then
            json_entry="${json_entry},\"detail\":\"$(json_escape "$detail")\"}"
        else
            json_entry="${json_entry}}"
        fi
        if [[ "$JSON_RESULTS" == "[]" ]]; then
            JSON_RESULTS="[${json_entry}]"
        else
            JSON_RESULTS="${JSON_RESULTS%]},$json_entry]"
        fi
        return
    fi

    # Terminal output
    local color=""
    case "$status" in
        FIXED)      color="$GREEN" ;;
        SKIPPED)    color="$YELLOW" ;;
        FAILED)     color="$RED" ;;
        DRY-RUN)    color="$CYAN" ;;
        INSTRUCTED) color="$GREEN" ;;
    esac

    printf "  ${color}%-10s${NC}  %-14s  %s" "$status" "$id" "$description"
    if [[ -n "$detail" ]]; then
        printf " — %s" "$detail"
    fi
    echo ""
}

# --- Helper: execute or dry-run a command ---
# Usage: run_fix_cmd DESCRIPTION CMD [ARGS...]
# Returns the exit code of the command (or 0 for dry-run)
# Sets LAST_FIX_ERROR with stderr on failure for inclusion in reports.
LAST_FIX_ERROR=""
run_fix_cmd() {
    local desc="$1"
    shift
    LAST_FIX_ERROR=""
    if $DRY_RUN; then
        if ! $JSON_OUTPUT; then
            # Strip internal wrapper names (run_as_user, run_fix_cmd) from display
            local display_cmd="$*"
            display_cmd="${display_cmd/#run_as_user /}"
            printf "    ${CYAN}[DRY-RUN]${NC} Would execute: %s\n" "$display_cmd"
        fi
        return 0
    fi
    local err_file
    err_file=$(mktemp /tmp/openclaw-fix-err.XXXXXX)
    if "$@" 2>"$err_file"; then
        rm -f "$err_file"
        return 0
    else
        LAST_FIX_ERROR=$(head -3 "$err_file" | tr '\n' ' ')
        rm -f "$err_file"
        return 1
    fi
}

# --- Helper: print instruction for manual fix ---
# Usage: print_instruction LINES...
print_instruction() {
    if $DRY_RUN; then
        if ! $JSON_OUTPUT; then
            printf "    %s[DRY-RUN]%s Would show instructions:\n" "$CYAN" "$NC"
            for line in "$@"; do
                printf "    %s|%s %s\n" "$CYAN" "$NC" "$line"
            done
        fi
        return 0
    fi
    if ! $JSON_OUTPUT; then
        printf "    %sManual action required:%s\n" "$YELLOW" "$NC"
        for line in "$@"; do
            printf "    %s|%s %s\n" "$YELLOW" "$NC" "$line"
        done
    fi
    return 0
}

# --- Prompt for CONFIRMATION check ---
# Returns 0 if user confirms, 1 if declined
prompt_confirm() {
    local id="$1"
    local desc="$2"
    local classification="${FIX_REGISTRY[$id]:-UNKNOWN}"

    if [[ "$MODE" == "auto" && "$classification" == "CONFIRMATION" ]]; then
        return 1  # Auto mode skips CONFIRMATION checks
    fi

    if [[ "$MODE" == "interactive" ]]; then
        if ! $JSON_OUTPUT; then
            printf "\n  ${YELLOW}[CONFIRMATION]${NC} %s: %s\n" "$id" "$desc"
            printf "  Apply this fix? [y/N] "
            local reply
            read -r reply </dev/tty 2>/dev/null || reply="n"
            case "$reply" in
                [yY]|[yY][eE][sS]) return 0 ;;
                *) return 1 ;;
            esac
        fi
    fi

    return 0
}


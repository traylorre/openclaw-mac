# --- JSON string escaper ---
# Escapes characters that would break JSON string values
json_escape() {
    local s="$1"
    s="${s//\\/\\\\}"
    s="${s//\"/\\\"}"
    s="${s//$'\n'/\\n}"
    s="${s//$'\t'/\\t}"
    printf '%s' "$s"
}

# --- User Scope ---
# When run with sudo, user-scoped defaults commands must target the
# invoking user, not root. SUDO_USER is set automatically by sudo.
ORIGINAL_USER="${SUDO_USER:-$(whoami)}"

# Run a command as the invoking user (drops root for user-scoped operations)
run_as_user() {
    if [[ $EUID -eq 0 && -n "${SUDO_USER:-}" ]]; then
        sudo -u "$SUDO_USER" "$@"
    else
        "$@"
    fi
}

# --- Pre-Fix Snapshot ---
# Captures current settings before modification so they can be selectively restored.
# Writes a standalone restore script that another user can run to undo specific changes.
SNAPSHOT_FILE=""
SNAPSHOT_COUNT=0

_init_snapshot() {
    local timestamp
    timestamp=$(date +%Y%m%d-%H%M%S)
    local snapshot_dir="${AUDIT_LOG_DIR}"
    if [[ ! -d "$snapshot_dir" || ! -w "$snapshot_dir" ]]; then
        snapshot_dir="/tmp"
    fi
    SNAPSHOT_FILE="${snapshot_dir}/pre-fix-restore-${timestamp}.sh"

    cat > "$SNAPSHOT_FILE" <<'SNAPSHOT_HEADER'
#!/usr/bin/env bash
# OpenClaw Pre-Fix Restore Script
# Generated by hardening-fix.sh before applying changes.
# Run specific functions to undo individual fixes, or run with --all.
#
# Usage:
#   bash restore-YYYYMMDD-HHMMSS.sh --list       # show available restores
#   bash restore-YYYYMMDD-HHMMSS.sh --all        # undo everything
#   bash restore-YYYYMMDD-HHMMSS.sh CHK-FIREWALL # undo one fix
set -euo pipefail

RED='\033[0;31m'; GREEN='\033[0;32m'; CYAN='\033[0;36m'; NC='\033[0m'
if [[ ! -t 1 ]]; then RED='' GREEN='' CYAN='' NC=''; fi

# Drop root for user-scoped settings (same as hardening-fix.sh)
run_as_user() {
    if [[ $EUID -eq 0 && -n "${SUDO_USER:-}" ]]; then
        sudo -u "$SUDO_USER" "$@"
    else
        "$@"
    fi
}

RESTORE_FUNCTIONS=()

run_restore() {
    local id="$1"; shift
    printf "  ${CYAN}RESTORE${NC}  %s\n" "$id"
    if "$@"; then
        printf "  ${GREEN}OK${NC}       %s restored\n" "$id"
    else
        printf "  ${RED}FAIL${NC}     %s restore failed\n" "$id"
    fi
}

SNAPSHOT_HEADER

    chmod 755 "$SNAPSHOT_FILE"
}

# Record the current value of a setting before changing it.
# Usage: snapshot_setting CHK-ID "description" RESTORE_COMMAND...
snapshot_setting() {
    local id="$1"
    local desc="$2"
    shift 2

    if [[ -z "$SNAPSHOT_FILE" ]]; then
        return
    fi
    if $DRY_RUN; then
        return
    fi

    SNAPSHOT_COUNT=$((SNAPSHOT_COUNT + 1))

    # Write a restore function for this check
    cat >> "$SNAPSHOT_FILE" <<RESTORE_FUNC

restore_${id//-/_}() {
    # ${desc}
    $*
}
RESTORE_FUNCTIONS+=("${id}")

RESTORE_FUNC
}

_finalize_snapshot() {
    if [[ -z "$SNAPSHOT_FILE" || $SNAPSHOT_COUNT -eq 0 ]]; then
        # No settings captured — remove empty snapshot
        rm -f "$SNAPSHOT_FILE"
        SNAPSHOT_FILE=""
        return
    fi

    # Write the main dispatch logic
    cat >> "$SNAPSHOT_FILE" <<'SNAPSHOT_FOOTER'

# --- Main ---
main() {
    if [[ $# -eq 0 || "$1" == "--help" ]]; then
        echo "Usage: $0 [--list | --all | CHK-ID...]"
        exit 0
    fi
    if [[ "$1" == "--list" ]]; then
        echo "Available restores:"
        for fn in "${RESTORE_FUNCTIONS[@]}"; do
            echo "  $fn"
        done
        exit 0
    fi
    if [[ "$1" == "--all" ]]; then
        for fn in "${RESTORE_FUNCTIONS[@]}"; do
            local func_name="restore_${fn//-/_}"
            run_restore "$fn" "$func_name"
        done
        exit 0
    fi
    for arg in "$@"; do
        local func_name="restore_${arg//-/_}"
        if declare -f "$func_name" &>/dev/null; then
            run_restore "$arg" "$func_name"
        else
            echo "Unknown check ID: $arg" >&2
        fi
    done
}

main "$@"
SNAPSHOT_FOOTER
}

